package com.yinhai.ta3.sysapp.consolemg.domain;

public class ConsoleModule {

	private Long moduleid;
	private String modulename;
	private String moduleurl;
	private String modulesta;
	private String moduledefault;
	private String moduleheight;

	public Long getModuleid() {
		return moduleid;
	}

	public void setModuleid(Long moduleid) {
		this.moduleid = moduleid;
	}

	public String getModulename() {
		return modulename;
	}

	public void setModulename(String modulename) {
		this.modulename = modulename;
	}

	public String getModuleurl() {
		return moduleurl;
	}

	public void setModuleurl(String moduleurl) {
		this.moduleurl = moduleurl;
	}

	public String getModulesta() {
		return modulesta;
	}

	public void setModulesta(String modulesta) {
		this.modulesta = modulesta;
	}

	public String getModuledefault() {
		return moduledefault;
	}

	public void setModuledefault(String moduledefault) {
		this.moduledefault = moduledefault;
	}

	public String getModuleheight() {
		return moduleheight;
	}

	public void setModuleheight(String moduleheight) {
		this.moduleheight = moduleheight;
	}
}
